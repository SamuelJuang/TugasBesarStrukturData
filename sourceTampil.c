int readPassword(char *pass, int length ){
	char input = '\0';
	int i=0;
	while( (input = getch()) != '\r' ) {
		if( input == '\b' ) {
			printf( "\b \b" );
			if( i > 0 ) {
				*pass-- = ' ';
				i--;
			}
			continue;
		}
		i++;
		if( i == length ) break;
		*pass++ = input;
		putchar( '*' );
	}
	*pass++ = '\0';
	putchar( '\n');
	return i;
}

int readLine( char* str ){
	char input = ' ';
	int len = 0;
	do {
		input = getche();
		if( input == 13 ) {
			*str = '\0';
			break;
		}
		else if( input == '\b' ) {
			printf( " \b \b" );
			*str--;
			len--;
			continue;
		}
		else {
			len++;
			*str++ = input;
		}
	}
	while( 1 );
	return len;
}

